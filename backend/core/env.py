"""Чтение и валидация переменных окружения с поведением fail-fast.

Модуль предоставляет средства для безопасного чтения конфигурации из окружения
(Требования 4.1, 4.2, 4.3, 5.1):

- ``require_env`` собирает ВСЕ отсутствующие/пустые обязательные переменные за
  один проход и бросает :class:`MissingEnvError` со списком их имён, что
  позволяет упасть до привязки к сетевому порту с полным перечислением проблем.
- ``get_bool`` интерпретирует булевы флаги (например, ``DEBUG``) консервативно:
  ``True`` только при точном значении ``"True"``, иначе ``False`` — включая
  случаи отсутствия переменной и пустой строки.
"""

import os

__all__ = ["MissingEnvError", "require_env", "get_bool"]


class MissingEnvError(RuntimeError):
    """Ошибка отсутствия обязательных переменных окружения.

    Несёт имена всех отсутствующих или пустых обязательных переменных в
    атрибуте :attr:`missing` (список в порядке запроса), чтобы вызывающая
    сторона и сообщение об ошибке точно перечисляли каждую проблему (R4.2).
    """

    def __init__(self, missing):
        self.missing = list(missing)
        names = ", ".join(self.missing)
        super().__init__(
            f"Missing required environment variable(s): {names}"
        )


def require_env(*names):
    """Прочитать обязательные переменные окружения, собрав все отсутствующие.

    Переменная считается отсутствующей, если она не задана или разрешается в
    пустую строку. Функция проверяет ВСЕ переданные имена за один проход и, если
    хотя бы одна переменная отсутствует, бросает :class:`MissingEnvError` со
    списком имён всех отсутствующих переменных (R4.1, R4.2, R5.1).

    Args:
        *names: Имена обязательных переменных окружения.

    Returns:
        dict: Отображение ``{имя: значение}`` для всех запрошенных переменных.

    Raises:
        MissingEnvError: Если хотя бы одна переменная отсутствует или пуста.
    """
    values = {}
    missing = []
    for name in names:
        value = os.environ.get(name)
        if value is None or value == "":
            missing.append(name)
        else:
            values[name] = value
    if missing:
        raise MissingEnvError(missing)
    return values


def get_bool(name, default=False):
    """Прочитать булеву переменную окружения консервативно.

    Возвращает ``True`` только если значение переменной в точности равно
    строке ``"True"``. Во всех остальных случаях — включая отсутствие
    переменной, пустую строку и любые другие значения — возвращает ``False``
    (R4.3). Параметр ``default`` используется, когда переменная не задана.

    Args:
        name: Имя переменной окружения.
        default: Значение по умолчанию, если переменная не задана.

    Returns:
        bool: ``True`` только при значении ``"True"``, иначе ``False``.
    """
    value = os.environ.get(name)
    if value is None:
        return default is True
    return value == "True"
